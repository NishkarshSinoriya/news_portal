                <p class="news_text">PHNOM PENH, Cambodia — It is the final stretch of campaign season in Cambodia. The dark-blue posters for Prime Minister Hun Sen and his Cambodian People’s Party are ubiquitous, seen from the sides of buildings in Phnom Penh to the billboards along the main roads in the Cambodian countryside. Yet somehow it still doesn’t feel like a parliamentary election is happening on Sunday in this country of 16 million. In large part, that’s because there are effectively no longer any independent news outlets left in Cambodia to cover it.</p>
                <p class="news_text">The descent has been rapid. In less than a year, more than 30 radio stations and The Cambodia Daily, one of Cambodia’s two independent, English-language newspapers, were shuttered; Radio Free Asia was chased out of the country and two of its reporters were arrested and charged with espionage; and The Phnom Penh Post, regarded as the country’s last remaining independent newspaper, was sold to Sivakumar S. Ganapathy, a Malaysian businessman with reported ties to Hun Sen’s government.</p>
                <p class="news_text">Cambodia is hardly the only country in Southeast Asia experiencing a crackdown on the free press. Never known as a bastion of journalistic freedom, the region has taken a sharply repressive turn, from the jailing of two Reuters journalists in Myanmar, to assaults from armies of online trolls in the Philippines, to the now-infamous Anti–Fake News Act in Malaysia, which imposed harsh penalties on anyone discovered to be spreading what the government deemed “fake news.” “When you take a glance across the region, all these threats and apparent deterioration in the press-freedom environment seem to be popping up all over the place,” Shawn Crispin, the Southeast Asia representative for the Committee to Protect Journalists, told me. “The situation was already pretty dire in many countries across the region, and oftentimes what we’re seeing is just kind of a piling-on effect.”</p>
